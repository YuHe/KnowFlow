async def seed_templates() -> None:
    # Ensure tables exist (idempotent)
    async with engine.begin() as conn:
        from app.database import Base  # noqa: F401

        await conn.run_sync(Base.metadata.create_all)

    async with AsyncSessionLocal() as db:
        created = 0
        skipped = 0

        for spec in TEMPLATES:
            result = await db.execute(
                select(DocumentTemplate).where(DocumentTemplate.name == spec.name)
            )
            existing = result.scalar_one_or_none()

            if existing is not None:
                print(f"[seed_templates] Skipping existing template: {spec.name!r}")
                skipped += 1
                continue

            tmpl = DocumentTemplate(
                name=spec.name,
                description=spec.description,
                category=spec.category,
                content_md=spec.content_md,
                content_html="",  # HTML rendering happens at API layer
                is_builtin=True,
                created_by=None,
            )
            db.add(tmpl)
            created += 1
            print(f"[seed_templates] Creating template: {spec.name!r}")

        if created:
            await db.commit()

        print(
            f"[seed_templates] Done. Created={created}, Skipped={skipped}."
        )


if __name__ == "__main__":
    asyncio.run(seed_templates())
    sys.exit(0)
